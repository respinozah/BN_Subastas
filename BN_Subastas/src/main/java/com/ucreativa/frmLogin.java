            if(gestor.esAdmin()){
                //Abro el form de admin
            }else{
                //Abro el form de usuario
                frmHomeUsuario homeUsuario = new frmHomeUsuario();
                homeUsuario.setVisible(true);
                this.setVisible(false);
                Repo.UsersRepo.gestor = this.gestor;
            }
